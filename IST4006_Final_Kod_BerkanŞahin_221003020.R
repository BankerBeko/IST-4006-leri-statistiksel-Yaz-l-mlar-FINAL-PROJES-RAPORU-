# ARR_DELAY i??in: alt s??n??r = Q1 - 1.5*IQR, ??st s??n??r = Q3 + 1.5*IQR
iqr_kirpma <- function(x) {
  Q1 <- quantile(x, 0.25, na.rm = TRUE)
  Q3 <- quantile(x, 0.75, na.rm = TRUE)
  IQR_val <- Q3 - Q1
  pmin(pmax(x, Q1 - 1.5 * IQR_val), Q3 + 1.5 * IQR_val)
}

